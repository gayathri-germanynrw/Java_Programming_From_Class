package m16_loops_part1;

public class ForLoop_Task_4 {
    public static void main(String[] args) {
        System.out.println("Number | Square of Number :\n");
        System.out.println("____________________________:\n");
        int j;
        for (int i = 1; i <=10; i++) {
            j = i * i;
            System.out.println(i + "\t\t|\t\t" + j);
        }
    }
}
