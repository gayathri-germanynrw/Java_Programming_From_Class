package m11_operators_part1;

public class SwapValues {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 10;
        int temp_num;
        temp_num = num1;
        num1 = num2;
        num2 = temp_num;
        System.out.println("num1------>" + num1);
        System.out.println("num2------>" + num2);

    }
}
